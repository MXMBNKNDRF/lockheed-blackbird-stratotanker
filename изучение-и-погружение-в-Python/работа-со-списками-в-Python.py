a[0] = 7
a
[7, 7, 5.5, 1000, 3.50, 200]

a.index(7)
0

a.count(7)
2

a.insert(2, 1000)
a
[7, 7, 1000, 5.5, 1000, 3.5, 200]

a.append(5.5)
a
